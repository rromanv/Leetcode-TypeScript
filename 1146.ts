class SnapshotArray {
  id: number
  history_records: Array<Array<[number, number]>>

  constructor(length: number) {
    this.id = 0
    this.history_records = Array.from({ length }, () => [[0, 0]])
  }

  set(index: number, val: number): void {
    this.history_records[index].push([this.id, val])
  }

  snap(): number {
    this.id += 1
    return this.id - 1
  }

  get(index: number, snap_id: number): number {
    let snap_index = this.history_records[index].findIndex(
      ([id, _]) => id > snap_id,
    )
    if (snap_index === -1) snap_index = this.history_records[index].length
    return this.history_records[index][snap_index - 1][1]
  }
}

